#!/usr/bin/env node

/**
 * Generate test fixture files — minimal valid image binaries,
 * mismatched files, corrupt files, SVG edge cases, and source files
 * that reference them in various ways.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const IMAGES = path.join(__dirname, 'fixtures/src/assets/images');
const PAGES = path.join(__dirname, 'fixtures/src/pages');

fs.mkdirSync(IMAGES, { recursive: true });
fs.mkdirSync(PAGES, { recursive: true });

// ── Minimal valid image buffers ─────────────────────────────────────

function minimalPng() {
  // 8-byte signature + IHDR chunk (1x1 px, 8-bit RGB)
  const sig = Buffer.from([0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a]);
  // IHDR: length=13, "IHDR", width=1, height=1, bitDepth=8, colorType=2, rest=0
  const ihdrData = Buffer.alloc(13);
  ihdrData.writeUInt32BE(1, 0);  // width
  ihdrData.writeUInt32BE(1, 4);  // height
  ihdrData[8] = 8;               // bit depth
  ihdrData[9] = 2;               // color type (RGB)
  const ihdrLen = Buffer.alloc(4);
  ihdrLen.writeUInt32BE(13, 0);
  const ihdrType = Buffer.from('IHDR');
  const ihdrCrc = Buffer.alloc(4); // dummy CRC
  // IEND chunk
  const iendLen = Buffer.alloc(4);
  const iendType = Buffer.from('IEND');
  const iendCrc = Buffer.alloc(4);
  return Buffer.concat([sig, ihdrLen, ihdrType, ihdrData, ihdrCrc, iendLen, iendType, iendCrc]);
}

function minimalJpeg() {
  // SOI + APP0 (JFIF) marker + EOI
  return Buffer.from([
    0xff, 0xd8,                         // SOI
    0xff, 0xe0,                         // APP0
    0x00, 0x10,                         // length = 16
    0x4a, 0x46, 0x49, 0x46, 0x00,      // "JFIF\0"
    0x01, 0x01,                         // version 1.1
    0x00,                               // aspect ratio units
    0x00, 0x01, 0x00, 0x01,            // 1x1 density
    0x00, 0x00,                         // no thumbnail
    0xff, 0xd9,                         // EOI
  ]);
}

function minimalGif() {
  // GIF89a, 1x1, no GCT
  return Buffer.from([
    0x47, 0x49, 0x46, 0x38, 0x39, 0x61, // GIF89a
    0x01, 0x00, 0x01, 0x00,             // 1x1
    0x00, 0x00, 0x00,                   // no GCT, bg=0, aspect=0
    0x3b,                               // trailer
  ]);
}

function minimalWebp() {
  // RIFF + WEBP + VP8 chunk (simplest lossy)
  const vp8Data = Buffer.from([
    0x9d, 0x01, 0x2a,  // VP8 bitstream signature
    0x01, 0x00,         // width=1
    0x01, 0x00,         // height=1
    0x01, 0x40, 0x25, 0xa4, 0x00, 0x03, 0x70, 0x00,
    0xfe, 0xfb, 0x94, 0x00, 0x00,
  ]);
  const riff = Buffer.from('RIFF');
  const webp = Buffer.from('WEBP');
  const vp8Tag = Buffer.from('VP8 ');
  const vp8Len = Buffer.alloc(4);
  vp8Len.writeUInt32LE(vp8Data.length, 0);
  const fileSize = Buffer.alloc(4);
  fileSize.writeUInt32LE(4 + 8 + vp8Data.length, 0); // "WEBP" + chunk header + data
  return Buffer.concat([riff, fileSize, webp, vp8Tag, vp8Len, vp8Data]);
}

function minimalAvif() {
  // Minimal ISOBMFF ftyp box with avif brand
  const brand = Buffer.from('avif');
  const ftypTag = Buffer.from('ftyp');
  const minorVersion = Buffer.alloc(4);
  const compatible = Buffer.from('mif1avif');
  const boxSize = Buffer.alloc(4);
  boxSize.writeUInt32BE(8 + 4 + 4 + compatible.length, 0); // size of ftyp box
  return Buffer.concat([boxSize, ftypTag, brand, minorVersion, compatible]);
}

// ── SVG variants ────────────────────────────────────────────────────

